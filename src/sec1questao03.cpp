/*
 Faca um programa que peca dois numeros e imprima a soma deles.
*/
#include <iostream>

using namespace std;

int main(){
    int A, B;

    cout << "Digite o primeiro valor: ";
    cin >> A;
    cout << "Digite o segundo valor: ";
    cin >> B;
    cout << "Resultado: " << A  + B << endl;

    return 0;
}
